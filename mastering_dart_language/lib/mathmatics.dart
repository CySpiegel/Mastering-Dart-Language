import 'dart:math';

double multiplication(double a, double b) {
  return a * b;
}

double division() {
  return 10 / 0;
}

num exponent() {
  return pow(2, 4);
}

num rounding(double a) {
  return a.roundToDouble();
}

num ceiling(double a) {
  return a.ceil();
}

num floor(double a) {
  return a.floor();
}
